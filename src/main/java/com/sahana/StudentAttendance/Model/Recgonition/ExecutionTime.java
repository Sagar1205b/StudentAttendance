package com.sahana.StudentAttendance.Model.Recgonition;

public class ExecutionTime {
    private double age;
    private double gender;
    private double detector;
    private double calculator;
    private double mask;

    // Getters and setters...

    public double getAge() {
        return age;
    }

    public void setAge(double age) {
        this.age = age;
    }

    public double getGender() {
        return gender;
    }

    public void setGender(double gender) {
        this.gender = gender;
    }

    public double getDetector() {
        return detector;
    }

    public void setDetector(double detector) {
        this.detector = detector;
    }

    public double getCalculator() {
        return calculator;
    }

    public void setCalculator(double calculator) {
        this.calculator = calculator;
    }

    public double getMask() {
        return mask;
    }

    public void setMask(double mask) {
        this.mask = mask;
    }
}

